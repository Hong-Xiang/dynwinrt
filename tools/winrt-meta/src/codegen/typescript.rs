/// Generate TS interface + _unpack/_pack helpers for a single struct type.
fn generate_struct_helpers(s: &TypeMeta) -> String {
    let (namespace, name, fields) = match s {
        TypeMeta::Struct { namespace, name, fields } => (namespace, name, fields),
        _ => return String::new(),
    };
    let mut out = String::new();

    // TS interface (exported)
    out.push_str(&format!("export interface {} {{\n", name));
    for f in fields {
        out.push_str(&format!("    {}: {};\n", to_camel_case(&f.name), ts_struct_field_type(&f.typ)));
    }
    out.push_str("}\n");

    // unpack function (exported)
    out.push_str(&format!("export function unpack{}(v: DynWinRtValue): {} {{\n", name, name));
    out.push_str("    const s = v.asStruct();\n");
    out.push_str("    return { ");
    let field_exprs: Vec<String> = fields.iter().enumerate().map(|(i, f)| {
        format!("{}: {}", to_camel_case(&f.name), struct_field_getter(&f.typ, i))
    }).collect();
    out.push_str(&field_exprs.join(", "));
    out.push_str(" };\n");
    out.push_str("}\n");
    // Internal alias for generated code
    out.push_str(&format!("const _unpack{0} = unpack{0};\n", name));

    // Type constant (exported)
    let full_name = format!("{}.{}", namespace, name);
    let field_types: Vec<String> = fields.iter().map(|f| ts_dynwinrt_type(&f.typ)).collect();
    out.push_str(&format!("export const {}_Type = DynWinRtType.structType('{}', [{}]);\n",
        name, full_name, field_types.join(", ")));
    // Internal alias for generated code
    out.push_str(&format!("const _{0}_Type = {0}_Type;\n", name));

    // pack function (exported)
    out.push_str(&format!("export function pack{}(v: {}): DynWinRtStruct {{\n", name, name));
    out.push_str(&format!("    const s = DynWinRtStruct.create({}_Type);\n", name));
    for (i, f) in fields.iter().enumerate() {
        out.push_str(&format!("    {};\n", struct_field_setter(&f.typ, i, &format!("v.{}", to_camel_case(&f.name)))));
    }
    out.push_str("    return s;\n");
    out.push_str("}\n");
    // Internal alias for generated code
    out.push_str(&format!("const _pack{0} = pack{0};\n", name));

    out
}

/// Returns the exported names for struct helpers of a given struct type (for TS index).
fn ts_struct_export_names(s: &TypeMeta) -> Vec<String> {
    match s {
        TypeMeta::Struct { name, .. } => vec![
            // Note: name itself is a TS interface (pure type), not exported in JS.
            format!("{}_Type", name),
            format!("pack{}", name),
            format!("unpack{}", name),
        ],
        _ => vec![],
    }
}

/// Generate a TypeScript file for a single RuntimeClass.
/// Uses `registerInterface` + `invoke` pattern.
pub fn generate_class(class: &ClassMeta, known_types: &HashSet<String>, delegate_type_names: &HashSet<String>, shared_iids: &HashSet<String>) -> String {
    // Collect structs used in all method signatures (needed for import and helpers)
    let used_structs = collect_used_structs_from_class(class);
    let has_structs = !used_structs.is_empty();

    let mut out = String::new();

    // Header
    out.push_str("// Generated by winrt-meta — do not edit\n");
    if has_structs {
        out.push_str("import { DynWinRtType, DynWinRtMethodSig, DynWinRtValue, DynWinRtArray, DynWinRtStruct, DynWinRtDelegate, WinGuid } from 'dynwinrt-js';\n");
    } else {
        out.push_str("import { DynWinRtType, DynWinRtMethodSig, DynWinRtValue, DynWinRtArray, DynWinRtDelegate, WinGuid } from 'dynwinrt-js';\n");
    }

    // Collect delegate names from all interfaces of this class
    let mut delegate_names: HashSet<String> = delegate_type_names.clone();
    let all_ifaces: Vec<&InterfaceMeta> = class.default_interface.iter()
        .chain(class.factory_interfaces.iter())
        .chain(class.static_interfaces.iter())
        .chain(class.required_interfaces.iter())
        .collect();
    for iface in &all_ifaces {
        for method in &iface.methods {
            for p in &method.params {
                match &p.typ {
                    TypeMeta::Delegate { name, .. } => { delegate_names.insert(name.clone()); }
                    _ => {}
                }
                if method.is_event_add || method.is_event_remove {
                    if let TypeMeta::Parameterized { name, args, .. } = &p.typ {
                        delegate_names.insert(crate::meta::make_parameterized_name(name, args));
                    }
                }
            }
        }
    }

    // Collection generics import (skip delegates)
    let collection_names = collect_used_generics_from_class(class);
    for cname in &collection_names {
        if !delegate_names.contains(cname) {
            out.push_str(&format!("import {{ {} }} from './{}';\n", cname, cname));
        }
    }

    // Import delegate IID + PARAM_TYPES for event methods
    let mut sorted_delegates: Vec<_> = delegate_names.iter().collect();
    sorted_delegates.sort();
    for dname in &sorted_delegates {
        out.push_str(&format!(
            "import {{ IID_{dname}, {dname}_PARAM_TYPES }} from './{dname}.js';\n",
        ));
    }

    // Type imports — only for types that have generated .ts files (skip delegates)
    // Track imported names to avoid redeclaring them as local constants
    let mut imported_names: HashSet<String> = HashSet::new();
    let imports = collect_type_imports(class);
    let mut sorted_imports: Vec<_> = imports.iter().collect();
    sorted_imports.sort_by(|a, b| (&a.namespace, &a.name, &a.kind).cmp(&(&b.namespace, &b.name, &b.kind)));
    for r in &sorted_imports {
        if known_types.contains(&r.name) && !delegate_names.contains(&r.name) {
            out.push_str(&format_type_import(&r.name, r.kind));
            imported_names.insert(r.name.clone());
            if r.kind == TypeKind::Interface {
                imported_names.insert(format!("IID_{}", r.name));
            }
        }
    }

    // Import shared required interfaces from their own files
    for req_iface in &class.required_interfaces {
        if !req_iface.iid.is_empty() && shared_iids.contains(&req_iface.iid) && !imported_names.contains(&req_iface.name) {
            out.push_str(&format_type_import(&req_iface.name, TypeKind::Interface));
            imported_names.insert(req_iface.name.clone());
            imported_names.insert(format!("IID_{}", req_iface.name));
        }
    }
    out.push('\n');

    // IID constants for all interfaces used by this class (skip if already imported)
    let all_class_ifaces: Vec<&InterfaceMeta> = class.default_interface.iter()
        .chain(class.factory_interfaces.iter())
        .chain(class.static_interfaces.iter())
        .chain(class.required_interfaces.iter())
        .collect();
    for iface in &all_class_ifaces {
        let iid_name = format!("IID_{}", iface.name);
        if !iface.iid.is_empty() && !imported_names.contains(&iid_name) {
            out.push_str(&format!(
                "const {} = WinGuid.parse('{}');\n",
                iid_name, iface.iid
            ));
        }
    }
    out.push('\n');

    // Interface registrations — all use _ prefix to avoid conflicts with imports
    if let Some(ref iface) = class.default_interface {
        out.push_str(&generate_interface_registration(iface, &format!("_{}", iface.name)));
        out.push('\n');
    }
    for iface in &class.factory_interfaces {
        out.push_str(&generate_interface_registration(iface, &format!("_{}", iface.name)));
        out.push('\n');
    }
    for iface in &class.static_interfaces {
        out.push_str(&generate_interface_registration(iface, &format!("_{}", iface.name)));
        out.push('\n');
    }
    for iface in &class.required_interfaces {
        // Skip inline registration for shared interfaces (imported from shared file)
        if !iface.iid.is_empty() && shared_iids.contains(&iface.iid) && imported_names.contains(&iface.name) {
            continue;
        }
        out.push_str(&generate_interface_registration(iface, &format!("_{}", iface.name)));
        out.push('\n');
    }

    // IActivationFactory for default constructor (ActivateInstance at vtable index 6)
    if class.has_default_constructor {
        out.push_str("const _IActivationFactory = DynWinRtType.registerInterface('IActivationFactory', WinGuid.parse('00000035-0000-0000-c000-000000000046'))\n    .addMethod('ActivateInstance', new DynWinRtMethodSig().addOut(DynWinRtType.object()));\n");
        out.push('\n');
    }

    // Struct interface + unpack/pack helpers
    for s in &used_structs {
        out.push_str(&generate_struct_helpers(s));
        out.push('\n');
    }

    // Class declaration
    out.push_str(&format!("export class {} {{\n", class.name));
    out.push_str("    readonly _obj: DynWinRtValue;\n");

    // Lazy-cached factory/static interface accessors
    let mut declared: HashSet<String> = HashSet::new();
    for iface in &class.factory_interfaces {
        let key = format!("f_{}", iface.name);
        if !iface.iid.is_empty() && declared.insert(key.clone()) {
            out.push_str(&format!("    private static _{k}: DynWinRtValue;\n", k = key));
            out.push_str(&format!(
                "    private static {k}() {{ return {cls}._{k} ??= DynWinRtValue.activationFactory('{full}').cast(IID_{iface}); }}\n",
                k = key, cls = class.name, iface = iface.name, full = class.full_name
            ));
        }
    }
    for iface in &class.static_interfaces {
        let key = format!("s_{}", iface.name);
        if !iface.iid.is_empty() && declared.insert(key.clone()) {
            out.push_str(&format!("    private static _{k}: DynWinRtValue;\n", k = key));
            out.push_str(&format!(
                "    private static {k}() {{ return {cls}._{k} ??= DynWinRtValue.activationFactory('{full}').cast(IID_{iface}); }}\n",
                k = key, cls = class.name, iface = iface.name, full = class.full_name
            ));
        }
    }

    out.push('\n');
    out.push_str("    constructor(obj: DynWinRtValue) {\n");
    if let Some(ref iface) = class.default_interface {
        if !iface.iid.is_empty() {
            out.push_str(&format!("        this._obj = obj.cast(IID_{});\n", iface.name));
        } else {
            out.push_str("        this._obj = obj;\n");
        }
    } else {
        out.push_str("        this._obj = obj;\n");
    }
    out.push_str("    }\n");

    // Default constructor
    if class.has_default_constructor {
        let has_create_factory = class.factory_interfaces.iter()
            .any(|iface| iface.methods.iter().any(|m| {
                let camel = to_camel_case(&m.name);
                camel == "create" || camel.starts_with("create")
            }));
        let ctor_name = if has_create_factory { "createDefault" } else { "create" };
        out.push('\n');
        out.push_str(&format!(
            "    static {}(): {} {{\n",
            ctor_name, class.name
        ));
        // activationFactory returns IActivationFactory; invoke ActivateInstance (vtable 6) to get instance
        out.push_str(&format!(
            "        return new {}(_IActivationFactory.method(6).invoke(DynWinRtValue.activationFactory('{}'), []));\n",
            class.name, class.full_name
        ));
        out.push_str("    }\n");
    }

    // Factory methods
    for iface in &class.factory_interfaces {
        for method in &iface.methods {
            out.push('\n');
            out.push_str(&generate_factory_method_invoke(class, iface, method, known_types));
        }
    }

    // Static methods
    for iface in &class.static_interfaces {
        for method in &iface.methods {
            out.push('\n');
            out.push_str(&generate_static_method_invoke(class, iface, method, known_types));
        }
    }

    // Instance methods: only default interface methods go directly on the class.
    // Non-default interface methods are accessed via .as(InterfaceWrapper).
    if let Some(ref default_iface) = class.default_interface {
        let iface_var = format!("_{}", default_iface.name);
        for method in &default_iface.methods {
            out.push('\n');
            out.push_str(&generate_iface_instance_method(default_iface, &iface_var, method, known_types, &delegate_type_names));
        }
    }

    // Auto-generate close() if class implements IClosable
    const ICLOSABLE_IID: &str = "30d5a829-7fa4-4026-83bb-d75bae4ea99e";
    if class.required_interfaces.iter().any(|ri| ri.iid == ICLOSABLE_IID) {
        out.push('\n');
        out.push_str("    close(): void {\n");
        out.push_str("        IClosable.from(this._obj).close();\n");
        out.push_str("    }\n");
    }

    // .as() method for accessing non-default interfaces
    // Uses static from() which does QI cast internally
    if !class.required_interfaces.is_empty() {
        out.push('\n');
        out.push_str("    as<T>(InterfaceClass: { from(obj: DynWinRtValue): T }): T {\n");
        out.push_str("        return InterfaceClass.from(this._obj);\n");
        out.push_str("    }\n");
    }

    out.push_str("}\n");

    // Generate inline wrapper classes for required interfaces (non-default)
    // so they can be used with .as()
    // Skip if already imported from its own file to avoid redeclaration.
    for req_iface in &class.required_interfaces {
        if req_iface.iid.is_empty() { continue; }
        if imported_names.contains(&req_iface.name) {
            // Already imported — re-exported automatically via the import
            continue;
        }
        let reg_var = format!("_{}", req_iface.name);
        out.push('\n');
        out.push_str(&format!("export class {} {{\n", req_iface.name));
        out.push_str("    readonly _obj: DynWinRtValue;\n\n");
        out.push_str("    constructor(obj: DynWinRtValue) {\n");
        out.push_str("        this._obj = obj;\n");
        out.push_str("    }\n\n");
        out.push_str(&format!(
            "    static from(obj: DynWinRtValue): {} {{\n",
            req_iface.name
        ));
        out.push_str(&format!(
            "        return new {}(obj.cast(IID_{}));\n",
            req_iface.name, req_iface.name
        ));
        out.push_str("    }\n");
        for method in &req_iface.methods {
            out.push('\n');
            out.push_str(&generate_iface_instance_method(req_iface, &reg_var, method, known_types, &delegate_type_names));
        }
        out.push_str("}\n");
    }
    out
}

/// Format an import line based on type kind.
/// - Interfaces export both IID and wrapper class
/// - Classes and enums only export the name (IIDs are internal)
fn format_type_import(name: &str, kind: TypeKind) -> String {
    if kind == TypeKind::Interface {
        format!("import {{ IID_{name}, {name} }} from './{name}.js';\n")
    } else {
        format!("import {{ {} }} from './{}';\n", name, name)
    }
}

